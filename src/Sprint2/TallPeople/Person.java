package Sprint2.TallPeople;

public class Person {
    protected String name;
    protected String address;
    protected int age;
    protected int weight;
    protected int height;


    public Person(String name, String address, int age, int weight, int height) {
        this.name = name;
        this.address = address;
        this.age = age;
        this.weight = weight;
        this.height = height;
    }
}
